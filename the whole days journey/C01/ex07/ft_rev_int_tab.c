#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i ;
	int	te ;
	int	j ;

	j = 0 ;
	i = size - 1 ;
	while (j < i)
	{
		te = tab [i];
		tab[i] = tab[j];
		tab[j] = te ;
		j++;
		i--;
	}
}
